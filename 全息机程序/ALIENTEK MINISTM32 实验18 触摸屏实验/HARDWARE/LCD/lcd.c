//..........延时n毫秒..........
void delay_n_ms(uint ms)
{
	uint i,j;
	for(i=0;i<ms;i++)
		for(j=0;j<123;j++);
}
//..........测忙..........
void chk_lcm_busy(void)
{
	uchar lcm_data;
	lcm_cs=0;
	lcm_a0=0;
	lcm_wr=1;
	/*input*/
	GPIOD->CRL = 0x88888888;
	lcm_rd=0;
	delay_n_ms(1);
	while((GPIOD->IDR)&0x40);
	lcm_rd=1;
	lcm_cs=0;
	/*output*/
	GPIOD->CRL = 0x33333333;
}
//..........写命令到LCM..........
void send_cmd_to_lcm(uchar lcm_cmd)
{
	chk_lcm_busy();
	lcm_cs=0;
	lcm_a0=1;
	lcm_rd=1;
	lcm_WriteByte=lcm_cmd;
	lcm_wr=0;
	delay_n_ms(1);
	lcm_wr=1;
	delay_n_ms(1);
	lcm_cs=1;
}
//..........写数据到LCM..........
void send_data_to_lcm(uchar lcm_data)
{
	chk_lcm_busy();
	lcm_cs=0;
	lcm_a0=0;
	lcm_rd=1;
	lcm_WriteByte=lcm_data;
	lcm_wr=0;
	delay_n_ms(1);
	lcm_wr=1;
	delay_n_ms(1);
	lcm_cs=1;
}
//..........写1个命令和1个数据到LCM..........
void send_cmd_and_data_to_lcm(uchar lcm_cmd,uchar lcm_data)
{
	send_cmd_to_lcm(lcm_cmd);
	send_data_to_lcm(lcm_data);
}
//..........写1个16进制数据到LCM..........
void send_two_data_to_lcm(uint lcm_data)
{
	send_data_to_lcm(lcm_data);
	send_data_to_lcm(lcm_data>>8);
}
//..........从LCM读数据..........
uchar read_data_from_lcm(void)
{
	uchar lcm_data;
	chk_lcm_busy();
	lcm_cs=0;
	lcm_a0=0;
	lcm_wr=1;
	/*input*/
	GPIOD->CRL = 0x88888888;
	lcm_rd=0;
	delay_n_ms(1);
	lcm_data=GPIOD->IDR;
	lcm_rd=1;
	delay_n_ms(1);
	lcm_cs=0;
	/*output*/
	GPIOD->CRL = 0x33333333;
	return lcm_data;
}
