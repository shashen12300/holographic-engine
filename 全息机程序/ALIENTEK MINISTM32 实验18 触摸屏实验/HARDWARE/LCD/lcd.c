//..........初始化..........
void lcm_init(void)
{
	uchar i;
	GPIO_InitTypeDef GPIO_InitStructure;
	/*开启相应时钟 */
 	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC|RCC_APB2Periph_GPIOD, ENABLE); 
	//GPIO_PinRemapConfig(GPIO_Remap_SWJ_JTAGDisable , ENABLE);
	
	/*控制信号*/
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10|GPIO_Pin_9|GPIO_Pin_8|GPIO_Pin_7|GPIO_Pin_6;	   //GPIO_Pin_10
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;  //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOC, &GPIO_InitStructure); //GPIOC
	GPIO_SetBits(GPIOC,GPIO_Pin_10|GPIO_Pin_9|GPIO_Pin_8|GPIO_Pin_7|GPIO_Pin_6);


	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_All;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;  //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOD, &GPIO_InitStructure); //GPIOB
	GPIO_SetBits(GPIOD,GPIO_Pin_All);
	
	lcm_rst=0;
	delay_n_ms(5);
	lcm_rst=1;
	delay_n_ms(50);
	lcm_cs=0;
	send_cmd_to_lcm(0x40);				//WRITE SYSTEMSET 指令和参数
	for(i=0;i<8;i++)
		send_data_to_lcm(systemset_tab[i]);
		
	send_cmd_to_lcm(0x40);				//WRITE SYSTEMSET 指令和参数
	for(i=0;i<8;i++)
		datalcd[i]=read_data_from_lcm();
		
	send_cmd_to_lcm(0x44);				//WRITE SCROLL 指令和参数
	for(i=0;i<10;i++)
		send_data_to_lcm(scroll_tab[i]);
	send_cmd_and_data_to_lcm(0x5a,0x00);		//显示画面水平移动量
	send_cmd_and_data_to_lcm(0x5b,0x00);		//显示属性
	send_cmd_to_lcm(0x5d);				//光标设置
	send_two_data_to_lcm(0x8f07);
	send_cmd_to_lcm(0x4c);				//光标自动移动方向--向右
	send_cmd_and_data_to_lcm(0x59,0x54);		//DISP ON
}
